package operators_for_loop;

public class bitwiseOperators {
    public static void main(String[] args) {
        // Bitwise Operators

        int a = 10, b = 8;

        System.out.println(a & b); // & Bitwise
        System.out.println(a | b); // OR Bitwise
        System.out.println(a ^ b); // XOR Bitwise
        System.out.println(a << 1); // Left shift (Double the value)
        System.out.println(a >> 1); // Right shift (Half the value)
        System.out.println(~a); // Tilt Bitwise Operator negative all the bits
    }
}
